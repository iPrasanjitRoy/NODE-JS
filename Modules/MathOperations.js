// Function TO Add Two Numbers 
function Add(a, b) {
  return a + b;
}

// Function To Subtract Two Numbers 
function Subtract(a, b) {
  return a - b;
}

// Function To Multiply Two Numbers 
function Multiply(a, b) {
  return a * b;
}


// Export The Functions So They Can Be Used In Other Files
module.exports = {
  Add,
  Subtract,
  Multiply,
};


